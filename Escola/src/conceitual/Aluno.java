package conceitual;

/**
 *
 * @author Wesley
 */
public class Aluno {

    private String nome;
    private int nderegistro;
    private float notas;

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the nderegistro
     */
    public int getNderegistro() {
        return nderegistro;
    }

    /**
     * @param nderegistro the nderegistro to set
     */
    public void setNderegistro(int nderegistro) {
        this.nderegistro = nderegistro;
    }

    /**
     * @return the notas
     */
    public float getNotas() {
        return notas;
    }

    /**
     * @param notas the notas to set
     */
    public void setNotas(float notas) {
        this.notas = notas;
    }
}
