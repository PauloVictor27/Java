package Conseitual;

/**
 *
 * @author UDESC
 */
public class Animal {
    private String animal_tipo;
    private String raca;
    private String sexo;
    private String pelagem;
    private int tamanho;
    private int peso;

    /**
     * @return the animal_tipo
     */
    public String getAnimal_tipo() {
        return animal_tipo;
    }

    /**
     * @param animal_tipo the animal_tipo to set
     */
    public void setAnimal_tipo(String animal_tipo) {
        this.animal_tipo = animal_tipo;
    }

    /**
     * @return the raca
     */
    public String getRaca() {
        return raca;
    }

    /**
     * @param raca the raca to set
     */
    public void setRaca(String raca) {
        this.raca = raca;
    }

    /**
     * @return the sexo
     */
    public String getSexo() {
        return sexo;
    }

    /**
     * @param sexo the sexo to set
     */
    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    /**
     * @return the pelagem
     */
    public String getPelagem() {
        return pelagem;
    }

    /**
     * @param pelagem the pelagem to set
     */
    public void setPelagem(String pelagem) {
        this.pelagem = pelagem;
    }

    /**
     * @return the tamanho
     */
    public int getTamanho() {
        return tamanho;
    }

    /**
     * @param tamanho the tamanho to set
     */
    public void setTamanho(int tamanho) {
        this.tamanho = tamanho;
    }

    /**
     * @return the peso
     */
    public int getPeso() {
        return peso;
    }

    /**
     * @param peso the peso to set
     */
    public void setPeso(int peso) {
        this.peso = peso;
    }
}
