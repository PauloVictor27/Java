package conseitual;

/**
 * Classe que descreve os atributos do adubo
 * @author UDESC
 */
public class Adubo {
private String tipoAdubo;

    /**
     * Obtem o tipo de adubo
     * @return the tipoAdubo
     */
    public String getTipoAdubo() {
        return tipoAdubo;
    }

    /**
     * Configura o tipo de adubo
     * @param tipoAdubo the tipoAdubo to set
     */
    public void setTipoAdubo(String tipoAdubo) {
        this.tipoAdubo = tipoAdubo;
    }
}
