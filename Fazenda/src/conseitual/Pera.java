package conseitual;

/**
 * Classe que descreve os atributos da pera
 * @author UDESC
 */
public class Pera {
    private int nPera;

    /**
     * Obtem o numero de peras
     * @return the nPera
     */
    public int getnPera() {
        return nPera;
    }

    /**
     * Configura o numero de peras
     * @param nPera the nPera to set
     */
    public void setnPera(int nPera) {
        this.nPera = nPera;
    }
}
