package conseitual;

/**
 * Classe que descreve os atributos da racao
 * @author UDESC
 */
public class Racao {
    private String tipoRacao;

    /**
     * Obtem o tipo da racao
     * @return the tipoRacao
     */
    public String getTipoRacao() {
        return tipoRacao;
    }

    /**
     * Configura o tipo da racao
     * @param tipoRacao the tipoRacao to set
     */
    public void setTipoRacao(String tipoRacao) {
        this.tipoRacao = tipoRacao;
    }
}
